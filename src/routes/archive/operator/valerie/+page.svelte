<p>
    Valerie Thorvaldsen was born in Reykjavík, Iceland on December 20th, 1976.
    As a little girl growing up on a farm, she surrounded herself with animals; 
    dogs, cats, sheep, reindeer and even puffins.  Her love for all living things 
    drove her to read all she could about medicine.  Despite her father's wishes
    for her to stay on the farm, she believed she could do more good in the world
    by becoming a scientist and developing cures for diseases.  In 1996, she graduated
    from the University of Iceland with a degree in biochemistry.
    <br/>
    <br/>
    Immediately after university, Valerie began working as a researcher for an international
    organization known as the International Health Institute  (or IHI for short).  At first, it was
    great.  But eventually, her work seemed less and less relevant to medicine and more to
    reckless experimentation.  One day, she witnessed a secret project involving an 
    unwilling human participant being injected with a black substance.  Before she could report
    this illegal experiment, she was captured by Naethra agents and was almost experimented on herself
    before she was rescued by Midguardian operators.  
    <br/>
    <br/>
    Due to this experience, Valerie decided to use her scientific knowledge for the Midguardians.
    Her custom armor is equipped with energy gauntlets that can be configured as energy shields or wrist-mounted
    blades.  If she puts her fists together, she can meld her two shields into one large force field.  
    Her role on the team is to provide mobile cover while the other operators move up.
</p>