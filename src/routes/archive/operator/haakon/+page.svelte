<p>
    Haakon Hagen was born in Tingsyrd, Sweden on July 7th, 1975.  He was the older
    brother to 4 younger sisters.  With so many siblings in the house, Haakon spent most of 
    his days outside.  During the long Swedish winters, he would skate for hours.  By age 8, 
    he was a competitive speed skater.  Eventually, Haakon took up other winter sports including
    hockey.  
    <br/>
    <br/>
    By 1994, he was widely regarded as the best player in the Swedish Ice Hockey Association and
    he was eventually drafted by the Toronto Maple Leafs to play in the NHL in 1996.  During his time in
    the limelight, he began to notice corruption in the industry.  He was pressured to promote
    questionable products.  One day, he pushed back too much and the Naethra attempted to "skin-steal" 
    him.  Before he could be killed, the Midguardians rescued him.  Haakon is now an operator and
    rescuing others like he was once rescued.
    <br/>
    <br/>
    Haakon is usually the first to the fight but rarely the one to end it.  He is all about protecting
    people and can swiftly arrive on scene thanks to his trusty plasma skates.  Since he was originally a hockey 
    player, it should come as no surprise that he prefers polearms as weapons.  His energy spear, named <i>Gungnir</i>,
    can be configured as a spear, axe or maul (or a hockey stick).
</p>