<h2>Ragnar Bergen</h2>

<img src={ragnarImg} alt="Profile picture of Ragnar Bergen">

<p>
    Ragnar Bergen was born in Oslo, Norway on January 1st, 1977.  He moved with his mother and 
    grandfather to the US in 1982.  One year, for his birthday, his grandfather gave him his
    first drum set.  Ragnar practiced for hours every day after school.  In 1995, he joined 
    a budding punk/nu metal band known as <i>Cog in the Machine.</i>  The band was an 
    overnight success.  
    <br/>
    <br/>
    They toured North America in 1996.  During the last concert of the tour, at Target Center in 
    Minneapolis, Ragnar noticed a bizarre change in the plan for the concert.  When he 
    followed up on it, led him to the horrifying discovery that his band mates had been 
    body swapped with Naethra agents.  After being rescued from a "skin-stealing" ritual 
    by the Midguardians, he decided to fight the Naethra as an operator.    
    <br/>
    <br/>
    Ragnar's fighting style is aggressive.  He charges into fights head-on with twin energy weapons
    that can be configured as axes or hammers depending on the occasion.  Despite being extremely
    rash, his decisions usually pan out (much to everyone's surprise).  He is the team leader on 
    every one of his assignments.
</p>